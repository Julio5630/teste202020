    private void btnCadastrarLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarLivroActionPerformed
        try {
        String nome = txtNomeLivro.getText();
        String genero = txtGenero.getText();
        String autor = txtAutor.getText();
        String editora = txtEditora.getText();
        String edicao = txtEdicao.getText();
        int quantidade = Integer.parseInt(txtQuantidade.getText());
        String isbn = txtISBN.getText();  // Vamos adicionar o campo ISBN

        // Verifica se o ISBN já existe
        java.sql.Connection con = Conexao.conexaoBanco();
        String sqlCheckISBN = "SELECT * FROM livro WHERE ISBN = ?";
        PreparedStatement stmtCheckISBN = con.prepareStatement(sqlCheckISBN);
        stmtCheckISBN.setString(1, isbn);
        ResultSet rs = stmtCheckISBN.executeQuery();

        if (rs.next()) {
            JOptionPane.showMessageDialog(null, "O ISBN já está cadastrado.");
        } else {
            // Se não existir, insere o novo livro
            String sqlInsert = "INSERT INTO livro (nome, genero, autor, editora, edicao, quantidade, ISBN) VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement stmtInsert = con.prepareStatement(sqlInsert);
            stmtInsert.setString(1, nome);
            stmtInsert.setString(2, genero);
            stmtInsert.setString(3, autor);
            stmtInsert.setString(4, editora);
            stmtInsert.setString(5, edicao);
            stmtInsert.setInt(6, quantidade);
            stmtInsert.setString(7, isbn);  // Insere o ISBN

            stmtInsert.executeUpdate();
            JOptionPane.showMessageDialog(null, "Livro cadastrado com sucesso!");
        }

        rs.close();
        stmtCheckISBN.close();
        con.close();

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Erro ao cadastrar livro: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnCadastrarLivroActionPerformed

