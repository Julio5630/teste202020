private void alterarLivro() {
   
    try {
        // Verifica se uma linha foi selecionada na tabela
        int selectedRow = tbLivro.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um livro na tabela para alterar.");
            return;
        }

        // Obtém o ID do livro da tabela (coluna 0)
        int idLivro = (int) tbLivro.getValueAt(selectedRow, 0);

        // Recupera os valores dos campos de entrada (TextFields)
        String nome = txtNomeLivro.getText();
        String genero = txtGenero.getText();
        String autor = txtAutor.getText();
        String editora = txtEditora.getText();
        String edicao = txtEdicao.getText();
        String quantidadeText = txtQuantidade.getText();
        String isbn = txtISBN.getText();

        // Validação simples para evitar valores vazios
        if (nome.isEmpty() || genero.isEmpty() || autor.isEmpty() || editora.isEmpty() || edicao.isEmpty() || quantidadeText.isEmpty() || isbn.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatórios!");
            return;
        }

        // Converte a quantidade para inteiro
        int quantidade;
        try {
            quantidade = Integer.parseInt(quantidadeText);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Quantidade deve ser um número válido.");
            return;
        }

        // Conexão com o banco de dados
        java.sql.Connection con = Conexao.conexaoBanco();

        // SQL para verificar se o ISBN já existe para outro livro
        String sqlCheckISBN = "SELECT * FROM livro WHERE ISBN = ? AND id_livro != ?";
        PreparedStatement stmtCheckISBN = con.prepareStatement(sqlCheckISBN);
        stmtCheckISBN.setString(1, isbn);
        stmtCheckISBN.setInt(2, idLivro); // Verifica se o ISBN já existe, mas não para o próprio livro
        ResultSet rs = stmtCheckISBN.executeQuery();

        if (rs.next()) {
            JOptionPane.showMessageDialog(null, "O ISBN já está cadastrado para outro livro.");
        } else {
            // SQL para atualizar o livro
            String sqlUpdate = "UPDATE livro SET nome = ?, genero = ?, autor = ?, editora = ?, edicao = ?, quantidade = ?, ISBN = ? WHERE id_livro = ?";
            PreparedStatement stmtUpdate = con.prepareStatement(sqlUpdate);
            stmtUpdate.setString(1, nome);
            stmtUpdate.setString(2, genero);
            stmtUpdate.setString(3, autor);
            stmtUpdate.setString(4, editora);
            stmtUpdate.setString(5, edicao);
            stmtUpdate.setInt(6, quantidade);
            stmtUpdate.setString(7, isbn);  // Altera o ISBN
            stmtUpdate.setInt(8, idLivro);  // Atualiza o livro com o ID informado

            // Executa a atualização
            int rowsUpdated = stmtUpdate.executeUpdate();

            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Livro alterado com sucesso!");
            } else {
                JOptionPane.showMessageDialog(null, "Nenhum livro foi alterado.");
            }

            stmtUpdate.close();
        }

        rs.close();
        stmtCheckISBN.close();
        con.close();

        // Atualiza a tabela (chame seu método de listar livros aqui)
        listarLivros(); // Método para atualizar a tabela de livros

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Erro ao alterar livro: " + ex.getMessage());
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(null, "Erro: Verifique se os campos de quantidade e valores numéricos estão corretos.");
    }
}


    
   private void listarLivros() {
    try {
        // Conexão com o banco de dados
        java.sql.Connection con = Conexao.conexaoBanco();

        // Consulta SQL para selecionar todos os livros
        String sql = "SELECT * FROM livro";
        PreparedStatement stmt = con.prepareStatement(sql);
        ResultSet rs = stmt.executeQuery();

        // Obtém o modelo da tabela
        DefaultTableModel model = (DefaultTableModel) tbLivro.getModel();

        // Limpa os dados existentes na tabela
        model.setRowCount(0);

        // Adiciona os registros ao modelo da tabela
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_livro"),           // ID do livro
                rs.getString("nome"),      // Nome do livro
                rs.getString("genero"),    // Gênero do livro
                rs.getString("autor"),     // Autor do livro
                rs.getString("editora"),   // Editora do livro
                rs.getString("edicao"),    // Edição do livro
                rs.getInt("quantidade"),   // Quantidade de livros
                rs.getString("ISBN")       // ISBN do livro
            });
        }

        // Fecha os recursos
        rs.close();
        stmt.close();
        con.close();

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Erro ao listar livros: " + ex.getMessage());
    }
}


    
    
